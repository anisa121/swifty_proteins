enum CPKCOlors: String {
    case hydrogen = "H"
    case carbon = "C"
    case nitrogen = "N"
    case oxygen = "O"
    case fluorine = "F"
    case chlorine = "Cl"
    case bromine = "Br"
    case iron = "Fe"
    case iodine = "I"
    case phosphorus = "P"
    case sulfur = "S"
    case titanium = "Ti"
    
//     noble gases (He, Ne, Ar, Kr, Xe, Rn
//     boron (B), most transition metals    beige
//     alkali metals (Li, Na, K, Rb, Cs, Fr)    violet
//     alkaline earth metals (Be, Mg, Ca, Sr, Ba, Ra)    dark green
}
